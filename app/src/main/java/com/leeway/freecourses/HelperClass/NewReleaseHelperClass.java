package com.leeway.freecourses.HelperClass;

public class NewReleaseHelperClass {

    int image;
    String title;

    public NewReleaseHelperClass(int image, String title) {
        this.image = image;
        this.title = title;
    }

    public int getImage() {
        return image;
    }

    public String getTitle() {
        return title;
    }

}
